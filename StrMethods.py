text = "hello python programming"

print("Original String: ", text)

print("1. Uppercase: ", text.upper())
print("2. Lowercase: ", text.lower())
print("3. Strip: ", text.strip())
print("4. Replace: ", text.replace("python", "java"))
print("5. Split: ", text.split(" "))
print("6. Capitalize: ", text.capitalize())
print("7. Title: ", text.title())
print("8. Count: ", text.count("o"))
print("9. Startswith: ", text.startswith("hello"))
print("10. Endswith: ", text.endswith("programming"))
print("11. Find: ", text.find("python"))
print("12. Index: ", text.index("python"))